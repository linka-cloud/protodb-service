func findServiceResource(service *protogen.Service, create, read, update, list, watch *protogen.Method) *protogen.Message {
	if create != nil {
		if _, msg, err := findResourceField(create.Input); err == nil {
			return msg
		}
	}
	if update != nil {
		if field := findFirstMessageField(update.Input); field != nil {
			return field.Message
		}
	}
	if read != nil {
		if field := findFirstMessageField(read.Output); field != nil {
			return field.Message
		}
	}
	if list != nil {
		if field := findFirstRepeatedMessageField(list.Output); field != nil {
			return field.Message
		}
	}
	if watch != nil {
		if field := findFieldByName(watch.Output, "new"); field != nil && field.Message != nil {
			return field.Message
		}
		if field := findFieldByName(watch.Output, "old"); field != nil && field.Message != nil {
			return field.Message
		}
	}
	return nil
}

func findFirstMessageField(message *protogen.Message) *protogen.Field {
	for _, field := range message.Fields {
		if field.Message != nil {
			return field
		}
	}
	return nil
}

func findFirstRepeatedMessageField(message *protogen.Message) *protogen.Field {
	for _, field := range message.Fields {
		if field.Message != nil && field.Desc.Cardinality() == protoreflect.Repeated {
			return field
		}
	}
	return nil
}

func findMethod(service *protogen.Service, name string) *protogen.Method {
	for _, method := range service.Methods {
		if string(method.Desc.Name()) == name {
			return method
		}
	}
	return nil
}

func findFieldByName(message *protogen.Message, name string) *protogen.Field {
	for _, field := range message.Fields {
		if string(field.Desc.Name()) == name {
			return field
		}
	}
	return nil
}

func findRequestKeyAccessor(request *protogen.Message, resource *protogen.Message) string {
	if request == nil || resource == nil {
		return ""
	}
	if path, ok := findKeyPath(resource); ok {
		if accessor, ok := accessorForPath(request, path); ok {
			return accessor
		}
		leaf := path[len(path)-1]
		if field := findFieldByName(request, leaf); field != nil {
			return "request." + field.GoName
		}
	}
	for _, name := range []string{"id", "key", "name"} {
		if field := findFieldByName(request, name); field != nil {
			return "request." + field.GoName
		}
	}
	return ""
}

func accessorForPath(message *protogen.Message, names []string) (string, bool) {
	if message == nil || len(names) == 0 {
		return "", false
	}
	current := message
	accessor := "request"
	for i, name := range names {
		field := findFieldByName(current, name)
		if field == nil {
			return "", false
		}
		accessor += "." + field.GoName
		if i == len(names)-1 {
			if field.Message != nil || field.Desc.IsMap() || field.Desc.Cardinality() == protoreflect.Repeated {
				return "", false
			}
			return accessor, true
		}
		if field.Message == nil || field.Desc.IsMap() || field.Desc.Cardinality() == protoreflect.Repeated {
			return "", false
		}
		current = field.Message
	}
	return "", false
}

func findKeyPath(message *protogen.Message) ([]string, bool) {
	if message == nil {
		return nil, false
	}
	stack := map[protoreflect.FullName]bool{message.Desc.FullName(): true}
	path, ok := findKeyPathIn(message, nil, stack)
	if !ok {
		return nil, false
	}
	names := make([]string, 0, len(path))
	for _, field := range path {
		names = append(names, string(field.Desc.Name()))
	}
	return names, true
}

func dotPath(path []string) string {
	out := ""
	for i, part := range path {
		if i != 0 {
			out += "."
		}
		out += part
	}
	return out
}

func findKeyPathIn(message *protogen.Message, path []*protogen.Field, stack map[protoreflect.FullName]bool) ([]*protogen.Field, bool) {
	for _, field := range message.Fields {
		next := make([]*protogen.Field, 0, len(path)+1)
		next = append(next, path...)
		next = append(next, field)
		if isExplicitKey(field) {
			return next, true
		}
		if field.Message == nil || field.Desc.IsMap() || field.Desc.Cardinality() == protoreflect.Repeated {
			continue
		}
		name := field.Message.Desc.FullName()
		if stack[name] {
			continue
		}
		stack[name] = true
		if nested, ok := findKeyPathIn(field.Message, next, stack); ok {
			return nested, true
		}
		delete(stack, name)
	}
	return nil, false
}

func isExplicitKey(field *protogen.Field) bool {
	if field == nil {
		return false
	}
	options, ok := field.Desc.Options().(*descriptorpb.FieldOptions)
	if !ok {
		return false
	}
	v := proto.GetExtension(options, protodbopts.E_Key)
	if v == nil {
		return false
	}
	b, ok := v.(bool)
	if !ok {
		return false
	}
	return b
}

func findResourceField(message *protogen.Message) (*protogen.Field, *protogen.Message, error) {
	var candidate *protogen.Field
	for _, field := range message.Fields {
		if field.Message == nil {
			continue
		}
		if string(field.Desc.Name()) == "resource" {
			return field, field.Message, nil
		}
		if candidate == nil {
			candidate = field
		}
	}
	if candidate == nil {
		return nil, nil, fmt.Errorf("create request must contain resource message")
	}
	return candidate, candidate.Message, nil
}

func findMessageField(message *protogen.Message, msg *protogen.Message) (*protogen.Field, error) {
	for _, field := range message.Fields {
		if field.Message == msg {
			return field, nil
		}
	}
	return nil, fmt.Errorf("message %s must contain %s field", message.GoIdent.GoName, msg.GoIdent.GoName)
}

func findRepeatedMessageField(message *protogen.Message, msg *protogen.Message) (*protogen.Field, error) {
	for _, field := range message.Fields {
		if field.Message == msg && field.Desc.Cardinality() == protoreflect.Repeated {
			return field, nil
		}
	}
	return nil, fmt.Errorf("message %s must contain repeated %s field", message.GoIdent.GoName, msg.GoIdent.GoName)
}

func serviceStructName(service *protogen.Service, multiple bool) string {
	if !multiple {
		return "DefaultService"
	}
	return "Default" + service.GoName + "Service"
}

func constructorName(service *protogen.Service, multiple bool) string {
	if !multiple {
		return "NewDefaultService"
	}
	return "NewDefault" + service.GoName + "Service"
}

func watchStreamTypeName(structName string, multiple bool) string {
	if !multiple {
		return "watchResponseStream"
	}
	return structName + "WatchResponseStream"
}
