const (
	importPathProtodbService = "go.linka.cloud/protodb-service"
	importPathProtodb        = "go.linka.cloud/protodb"
	importPathProtodbPB      = "go.linka.cloud/protodb/protodb/v1alpha1"
	importPathGrpc           = "google.golang.org/grpc"
	importPathContext        = "context"
	importPathFilters        = "go.linka.cloud/protofilters/filters"
)

func main() {
	protogen.Options{}.Run(func(plugin *protogen.Plugin) error {
		for _, file := range plugin.Files {
			if !file.Generate {
				continue
			}
			if len(file.Services) == 0 {
				continue
			}
			if err := generateFile(plugin, file); err != nil {
				return err
			}
		}
		return nil
	})
}

func generateFile(plugin *protogen.Plugin, file *protogen.File) error {
	filename := file.GeneratedFilenamePrefix + "_protodb.pb.go"
	g := plugin.NewGeneratedFile(filename, file.GoImportPath)
	g.P("// Code generated by protoc-gen-protodb-service. DO NOT EDIT.")
	g.P("package ", file.GoPackageName)
	g.P()

	multiple := len(file.Services) > 1
	for _, service := range file.Services {
		if err := generateService(g, file, service, multiple); err != nil {
			return err
		}
		g.P()
	}
	return nil
}

