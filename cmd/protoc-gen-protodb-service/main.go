		g.P("\tif err := s.Service.Delete(ctx, ", deleteKeyAccessor, "); err != nil {")
		g.P("\t\treturn nil, err")
		g.P("\t}")
		g.P("\treturn &", deleteMethod.Output.GoIdent.GoName, "{}, nil")
